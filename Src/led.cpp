LED::LED(GPIO_TypeDef* gpio, uint16_t pin)
{
    m_gpio = gpio;
    m_pin = pin;
}

void LED::On()
{
    HAL_GPIO_WritePin(m_gpio, m_pin, GPIO_PIN_SET);
}

void LED::Off()
{
    HAL_GPIO_WritePin(m_gpio, m_pin, GPIO_PIN_RESET);
}

void LED::Toggle()
{
    HAL_GPIO_TogglePin(m_gpio, m_pin);
}
